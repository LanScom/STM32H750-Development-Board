void SDIO_Test(void)
{
  FRESULT res;                                          		/* FatFs function common result code */
  uint32_t byteswritten, bytesread;                     		/* File write/read counts */
  uint8_t wtext[] = "This is STM32H750 working with FatFs"; /* File write buffer */
  TCHAR* fname = "STM32H750VBT6 Demo Board.TXT"; 						/* File Name */
  uint8_t rtext[100];                                   		/* File read buffer */

  if(f_mount(&SDFatFS, (TCHAR const*)SDPath, 0) != FR_OK)
	{
		/* FatFs Initialization Error */
		Error_Handler();
	}
	else
	{
		/*##-4- Create and Open a new text file object with write access #####*/
		if(f_open(&MyFile, fname, FA_CREATE_ALWAYS | FA_WRITE) != FR_OK)
		{
			Error_Handler();
		}
		else
		{
			/*##-5- Write data to the text file ################################*/
			res = f_write(&MyFile, wtext, sizeof(wtext), (void *)&byteswritten);

			if((byteswritten == 0) || (res != FR_OK))
			{
				Error_Handler();
			}
			else
			{
				/*##-6- Close the open text file #################################*/
				f_close(&MyFile);

				/*##-7- Open the text file object with read access ###############*/
				if(f_open(&MyFile, fname, FA_READ) != FR_OK)
				{
					Error_Handler();
				}
				else
				{
					/*##-8- Read data from the text file ###########################*/
					res = f_read(&MyFile, rtext, sizeof(rtext), (UINT*)&bytesread);

					if((bytesread == 0) || (res != FR_OK)) /* EOF or Error */
					{
						Error_Handler();
					}
					else
					{
						/*##-9- Close the open text file #############################*/
						f_close(&MyFile);

						/*##-10- Compare read data with the expected data ############*/
						if ((bytesread != byteswritten))
						{
							/* Read data is different from the expected data */
							Error_Handler();
						}
						else
						{
							/* Success of the demo: no error occurrence */
							// BSP_LED_On(LED1);
							rest = 1000;
						}
					}
				}
			}
		}
	}
}

